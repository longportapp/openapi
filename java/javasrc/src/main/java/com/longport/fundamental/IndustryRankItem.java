package com.longport.fundamental;

/** One ranked industry item. */
public class IndustryRankItem {
    /** Industry / sector name */
    public String name;
    /** Counter ID of the industry */
    public String counterId;
    /** Change percentage */
    public String chg;
    /** Name of the leading stock */
    public String leadingName;
    /** Ticker of the leading stock */
    public String leadingTicker;
    /** Change percentage of the leading stock */
    public String leadingChg;
    /** Value label name */
    public String valueName;
    /** Value data */
    public String valueData;
}
